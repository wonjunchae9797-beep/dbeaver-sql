-- ============================================
-- 4. ANIMAL (동물)
-- ============================================
CREATE TABLE ANIMAL (
    ANIMAL_ID    CHAR(3)        NOT NULL,
    ANIMAL_NAME  VARCHAR2(20)   NOT NULL,
    SPECIES_ID   CHAR(3)        NOT NULL,
    ZONE_ID      CHAR(2)        NOT NULL,
    KEEPER_ID    CHAR(3),
    GENDER       CHAR(1)        NOT NULL,
    WEIGHT_KG    NUMBER         NOT NULL,
    BIRTH_DATE   DATE           NOT NULL,
    ARRIVAL_DATE DATE,
    CONSTRAINT ANIMAL_PK PRIMARY KEY (ANIMAL_ID),
    CONSTRAINT ANIMAL_SPECIES_FK FOREIGN KEY (SPECIES_ID) REFERENCES SPECIES (SPECIES_ID),
    CONSTRAINT ANIMAL_ZONE_FK    FOREIGN KEY (ZONE_ID)    REFERENCES ZONE (ZONE_ID),
    CONSTRAINT ANIMAL_KEEPER_FK  FOREIGN KEY (KEEPER_ID)  REFERENCES KEEPER (KEEPER_ID),
    CONSTRAINT ANIMAL_GENDER_CK  CHECK (GENDER IN ('M', 'F'))
);

COMMENT ON TABLE  ANIMAL               IS '동물';
COMMENT ON COLUMN ANIMAL.ANIMAL_ID      IS '동물코드';
COMMENT ON COLUMN ANIMAL.ANIMAL_NAME    IS '이름';
COMMENT ON COLUMN ANIMAL.SPECIES_ID     IS '종코드';
COMMENT ON COLUMN ANIMAL.ZONE_ID        IS '구역코드';
COMMENT ON COLUMN ANIMAL.KEEPER_ID      IS '담당사육사코드';
COMMENT ON COLUMN ANIMAL.GENDER         IS '성별(M/F)';
COMMENT ON COLUMN ANIMAL.WEIGHT_KG      IS '체중(kg)';
