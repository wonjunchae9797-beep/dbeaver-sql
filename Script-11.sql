/*
 * 단순 뷰(1개 테이블, 함수/그룹핑 없음) 는 DML 가능
 * 복합 뷰(JOIN, GROUP BY등) DML불가
 * 보통 만들 때 WITH READ ONLY 옵션을 줘서 DML자체를 차단
 */

-- 구역별 통게 SELECT
CREATE OR REPLACE VIEW VW_ZONE_STATS
AS
SELECT
      ZONE_NAME
     ,COUNT(*) "COUNT"
     ,MAX(WEIGHT_KG) "MAX_WEIGHT"
     ,MIN(WEIGHT_KG) "MIN_WEIGHT"
  FROM 
      ANIMAL
  JOIN 
      ZONE USING(ZONE_ID)
 GROUP  
   BY
      ZONE_NAME;
WITH READ ONLY;

SELECT * FROM VW_ZONE_STATS;

 