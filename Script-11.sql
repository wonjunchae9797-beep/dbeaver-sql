-- < 뷰 만들기 >
CREATE VIEW VW_ANIMAL_INFO
AS
SELECT
      K.KEEPER_NAME
     ,A.ANIMAL_NAME
     ,S.SPECIES_NAME
     ,Z.ZONE_NAME
     ,A.WEIGHT_KG
     ,DECODE(A.GENDER, 'M', '수컷', 'F', '암것') "성별"
  FROM
      KEEPER K
  LEFT    
      ANIMAL A USING(KEEPER_ID)
  LEFT
  JOIN
      ZONE Z ON USING(A.ZONE_ID = Z.ZONE_ID)
  LEFT
  JOIN
      SPECIES S USING(SPECIES_ID)

SELECT * FROM VW_ANIMAL_INFO
 WHERE KEEPER_NAME = '제인구달'
 OREDER
    BY ANIMAL_NAME;

